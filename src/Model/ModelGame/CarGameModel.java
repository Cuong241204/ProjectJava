package Model.ModelGame;

public class CarGameModel {
    private int barrierCarX, barrierCarY, barrierFenceX, barrierFenceY, carX, carY, coinX, coinY;

    public CarGameModel() {
        this.barrierCarX = barrierCarX;
        this.barrierCarY = barrierCarY;
        this.barrierFenceX = barrierFenceX;
        this.barrierFenceY = barrierFenceY;
        this.carX = carX;
        this.carY = carY;
        this.coinX = coinX;
        this.coinY = coinY;
    }

    public int getBarrierCarX() {
        return barrierCarX;
    }

    public void setBarrierCarX(int barrierCarX) {
        this.barrierCarX = barrierCarX;
    }

    public int getBarrierCarY() {
        return barrierCarY;
    }

    public void setBarrierCarY(int barrierCarY) {
        this.barrierCarY = barrierCarY;
    }

    public int getBarrierFenceX() {
        return barrierFenceX;
    }

    public void setBarrierFenceX(int barrierFenceX) {
        this.barrierFenceX = barrierFenceX;
    }

    public int getBarrierFenceY() {
        return barrierFenceY;
    }

    public void setBarrierFenceY(int barrierFenceY) {
        this.barrierFenceY = barrierFenceY;
    }

    public int getCarX() {
        return carX;
    }

    public void setCarX(int carX) {
        this.carX = carX;
    }

    public int getCarY() {
        return carY;
    }

    public void setCarY(int carY) {
        this.carY = carY;
    }

    public int getCoinX() {
        return coinX;
    }

    public void setCoinX(int coinX) {
        this.coinX = coinX;
    }

    public int getCoinY() {
        return coinY;
    }

    public void setCoinY(int coinY) {
        this.coinY = coinY;
    }

    

    
}
